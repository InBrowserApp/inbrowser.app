<script setup lang="ts">
import { ref, watch } from 'vue'
import { NInput, NCard } from 'naive-ui'
import { ToolSectionHeader, ToolSection } from '@shared/ui/tool'
import { CopyToClipboardButton } from '@shared/ui/base'
import WhatIsRomanNumeral from './WhatIsRomanNumeral.vue'

// State
const arabicInput = ref('')
const romanInput = ref('')
const arabicInputStatus = ref<'success' | 'warning' | 'error' | undefined>()
const romanInputStatus = ref<'success' | 'warning' | 'error' | undefined>()

// Roman numeral conversion mappings
const romanValues = [
  { value: 1000, symbol: 'M' },
  { value: 900, symbol: 'CM' },
  { value: 500, symbol: 'D' },
  { value: 400, symbol: 'CD' },
  { value: 100, symbol: 'C' },
  { value: 90, symbol: 'XC' },
  { value: 50, symbol: 'L' },
  { value: 40, symbol: 'XL' },
  { value: 10, symbol: 'X' },
  { value: 9, symbol: 'IX' },
  { value: 5, symbol: 'V' },
  { value: 4, symbol: 'IV' },
  { value: 1, symbol: 'I' }
]

// Convert Arabic number to Roman numeral
function arabicToRoman(num: number): string {
  if (num < 1 || num > 3999) {
    throw new Error('Number out of range')
  }

  let result = ''
  let remaining = num

  for (const { value, symbol } of romanValues) {
    while (remaining >= value) {
      result += symbol
      remaining -= value
    }
  }

  return result
}

// Convert Roman numeral to Arabic number
function romanToArabic(roman: string): number {
  const romanMap: Record<string, number> = {
    I: 1,
    V: 5,
    X: 10,
    L: 50,
    C: 100,
    D: 500,
    M: 1000
  }

  const cleanRoman = roman.toUpperCase().trim()
  
  // Validate Roman numeral format
  if (!/^[IVXLCDM]+$/.test(cleanRoman)) {
    throw new Error('Invalid Roman numeral')
  }

  let result = 0
  let prevValue = 0

  for (let i = cleanRoman.length - 1; i >= 0; i--) {
    const currentValue = romanMap[cleanRoman[i]]
    
    if (!currentValue) {
      throw new Error('Invalid Roman numeral character')
    }

    if (currentValue < prevValue) {
      result -= currentValue
    } else {
      result += currentValue
    }
    
    prevValue = currentValue
  }

  // Validate by converting back and checking if it matches
  if (result < 1 || result > 3999 || arabicToRoman(result) !== cleanRoman) {
    throw new Error('Invalid Roman numeral format')
  }

  return result
}

// Watch Arabic input and convert to Roman
watch(arabicInput, (newValue) => {
  if (!newValue) {
    romanInput.value = ''
    arabicInputStatus.value = undefined
    return
  }
  
  const num = parseInt(newValue)
  
  if (isNaN(num) || num < 1 || num > 3999) {
    arabicInputStatus.value = 'error'
    romanInput.value = ''
  } else {
    arabicInputStatus.value = 'success'
    try {
      romanInput.value = arabicToRoman(num)
    } catch {
      arabicInputStatus.value = 'error'
      romanInput.value = ''
    }
  }
})

// Watch Roman input and convert to Arabic
watch(romanInput, (newValue) => {
  if (!newValue) {
    arabicInput.value = ''
    romanInputStatus.value = undefined
    return
  }

  try {
    const result = romanToArabic(newValue)
    arabicInput.value = result.toString()
    romanInputStatus.value = 'success'
  } catch {
    romanInputStatus.value = 'error'
    arabicInput.value = ''
  }
})
